CREATE TABLE BORROW
(
    ID               UUID      DEFAULT uuid_in((md5((random())::TEXT))::CSTRING),
    CREATE_TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATE_TIMESTAMP    TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_ID UUID NOT NULL ,
    BOOK_ID UUID NOT NULL,
    BORROWED_FROM TIMESTAMP,
    BORROWED_TO TIMESTAMP,
    PRIMARY KEY ( ID ),
    CONSTRAINT "USER_ID_FK" FOREIGN KEY (USER_ID) REFERENCES "library"."user" (ID),
    CONSTRAINT "BOOK_ID_FK" FOREIGN KEY (BOOK_ID) REFERENCES BOOK (ID)
);